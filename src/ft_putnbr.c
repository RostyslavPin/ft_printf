#include "ft_printf.h"

void	ft_putnbr(int n)
{
	if (n >= -2147483648 && n <= 2147483647)
	{
		if (n < 0)
		{
			ft_putchar('-');
			if (n == -2147483648)
			{
				ft_putchar('2');
				n = n % 1000000000;
			}
			n = -n;
		}
		if (n > 9)
		{
			ft_putnbr(n / 10);
			ft_putnbr(n % 10);
		}
		else
			ft_putchar(n + '0');
	}
}
